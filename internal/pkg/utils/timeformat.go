package utils

// 时间格式常量，Go中的时间格式化比较特殊，必须使用2006-01-02 15:04:05这个特定时间
// 下面定义了一些常用格式，使用更易读的常量名

const (
	// 完整日期时间格式，精确到毫秒
	DateTimeMillisecond = "2006-01-02 15:04:05.000"

	// 完整日期时间格式
	DateTime = "2006-01-02 15:04:05"

	// 仅日期格式
	DateOnly = "2006-01-02"

	// 仅时间格式
	TimeOnly = "15:04:05"

	// 年月格式
	YearMonth = "2006-01"

	// 月日格式
	MonthDay = "01-02"

	// 带斜杠的日期时间格式（精确到毫秒）
	SlashDateTimeMillisecond = "2006/01/02 15:04:05.000"

	// 带斜杠的日期时间格式
	SlashDateTime = "2006/01/02 15:04:05"

	// 带斜杠的日期格式
	SlashDateOnly = "2006/01/02"
)
